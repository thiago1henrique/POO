package revisao_prova_u1.exercicio_02;

public class Conversor {
    public void converter(int horas) {
        System.out.println(horas + " horas convertido para minutos são:" + (horas * 60) + " minutos");
        System.out.println(horas + " convertido para segundos são:" + (horas * 3600) + " segundos");
    }
}
